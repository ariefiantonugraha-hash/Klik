const translatableElements = document.querySelectorAll("[data-i18n]");
const languageButtons = document.querySelectorAll("[data-language]");
const mainNavToggle = document.getElementById("mainNavToggle");
const mainNavPanel = document.getElementById("mainNavPanel");

function setLanguage(language) {
  const dictionary = translations[language];

  translatableElements.forEach((element) => {
    const key = element.dataset.i18n;
    const value = dictionary[key];

    if (value) {
      element.textContent = value;
    }
  });

  languageButtons.forEach((button) => {
    button.classList.toggle("is-active", button.dataset.language === language);
  });

  document.documentElement.lang = language;
}

languageButtons.forEach((button) => {
  button.addEventListener("click", () => {
    setLanguage(button.dataset.language);
  });
});

if (mainNavToggle && mainNavPanel) {
  mainNavToggle.addEventListener("click", () => {
    const isOpen = mainNavToggle.getAttribute("aria-expanded") === "true";
    mainNavToggle.setAttribute("aria-expanded", String(!isOpen));
    mainNavPanel.classList.toggle("is-open", !isOpen);
    document.body.classList.toggle("menu-open", !isOpen);
  });

  mainNavPanel.querySelectorAll("a").forEach((link) => {
    link.addEventListener("click", () => {
      mainNavToggle.setAttribute("aria-expanded", "false");
      mainNavPanel.classList.remove("is-open");
      document.body.classList.remove("menu-open");
    });
  });
}

setLanguage("en");
